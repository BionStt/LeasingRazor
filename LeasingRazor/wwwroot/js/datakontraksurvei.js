$('#LuasUsaha').keypress(function (e) {
    var key = e.which || e.keyCode;
    if (!e.shiftKey && !e.altKey && !e.ctrlKey &&
        // numbers
        key >= 48 && key <= 57 ||
        // Numeric keypad
        //  key >= 96 && key <= 105 ||
        // Backspace and Tab and Enter
        key == 8 || key == 9 || key == 13 ||
        // Home and End
        key == 35 || key == 36 ||
        // left and right arrows
        key == 37 || //key == 39 ||
        // . and ,
        key == 190 || key == 188 ||
        // ATAS and BAWAH
        key == 24 || key == 25 ||
        // KANAN and KIRI
        key == 26 || key == 27 ||
        // Del and Ins
        key == 46 || key == 45)
        return true;

    return false;
});
